package com.kirosys.model;

public class PlayerDetailsInfo {

	String id;
	String teamName;
	String playerName;
	String playerType;
	String playerPoint;
	String selectionPercent;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTeamName() {
		return teamName;
	}
	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public String getPlayerType() {
		return playerType;
	}
	public void setPlayerType(String playerType) {
		this.playerType = playerType;
	}
	public String getPlayerPoint() {
		return playerPoint;
	}
	public void setPlayerPoint(String playerPoint) {
		this.playerPoint = playerPoint;
	}
	public String getSelectionPercent() {
		return selectionPercent;
	}
	public void setSelectionPercent(String selectionPercent) {
		this.selectionPercent = selectionPercent;
	}
	
}